import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const tsGameRoot = path.resolve(scriptDir, '..');
const repoRoot = path.resolve(tsGameRoot, '..');
const decompMapsDir = path.join(repoRoot, 'data/maps');
const runtimeMapsDir = path.join(tsGameRoot, 'src/world/maps');
const registryPath = path.join(tsGameRoot, 'src/world/mapRegistry.ts');

const LEGACY_NON_DECOMP_MAP_FILES = new Set(['prototypeRoute.json']);

const readJson = (filePath) => JSON.parse(fs.readFileSync(filePath, 'utf8'));

const toImportName = (fileBaseName, index) => {
  const words = fileBaseName.match(/[A-Za-z0-9]+/gu) ?? [`map${index}`];
  const identifier = words
    .map((word, wordIndex) => {
      const normalized = word.replace(/^[0-9]+/u, '');
      if (normalized.length === 0) return '';
      const first = normalized[0];
      const rest = normalized.slice(1);
      return wordIndex === 0
        ? `${first.toLowerCase()}${rest}`
        : `${first.toUpperCase()}${rest}`;
    })
    .join('');

  return identifier.length > 0 ? `${identifier}MapJson` : `map${index}Json`;
};

const toRuntimeFileBaseName = (decompName) => decompName
  .split('_')
  .filter((part) => part.length > 0)
  .map((part, index) => index === 0 ? `${part[0].toLowerCase()}${part.slice(1)}` : part)
  .join('');

const collectDecompMaps = () => fs.readdirSync(decompMapsDir)
  .flatMap((folderName) => {
    const mapJsonPath = path.join(decompMapsDir, folderName, 'map.json');
    if (!fs.existsSync(mapJsonPath)) return [];

    const map = readJson(mapJsonPath);
    if (typeof map.id !== 'string' || typeof map.name !== 'string') {
      throw new Error(`Decomp map ${folderName} is missing string id/name fields.`);
    }

    return [{ id: map.id, name: map.name, folderName }];
  })
  .sort((a, b) => a.id.localeCompare(b.id));

const collectRuntimeMapCandidates = () => fs.readdirSync(runtimeMapsDir)
  .filter((fileName) => fileName.endsWith('.json') && !LEGACY_NON_DECOMP_MAP_FILES.has(fileName))
  .sort()
  .map((fileName, index) => {
    const source = readJson(path.join(runtimeMapsDir, fileName));
    if (typeof source.id !== 'string') {
      throw new Error(`Runtime map ${fileName} is missing a string id field.`);
    }

    return {
      fileName,
      id: source.id,
      name: typeof source.metadata?.name === 'string' ? source.metadata.name : source.id,
      importName: toImportName(path.basename(fileName, '.json'), index)
    };
  });

const chooseRuntimeMap = (candidates) => {
  const exactName = `${toRuntimeFileBaseName(candidates[0].name)}.json`;
  return candidates.find((entry) => entry.fileName === exactName) ?? candidates[0];
};

const collectRuntimeMaps = () => {
  const byId = new Map();
  for (const candidate of collectRuntimeMapCandidates()) {
    byId.set(candidate.id, [...(byId.get(candidate.id) ?? []), candidate]);
  }

  return [...byId.values()].map(chooseRuntimeMap).sort((a, b) => a.id.localeCompare(b.id));
};

const assertUnique = (entries, key, label) => {
  const seen = new Map();
  for (const entry of entries) {
    const value = entry[key];
    const existing = seen.get(value);
    if (existing) {
      throw new Error(`Duplicate ${label} ${value}: ${existing.fileName ?? existing.folderName} and ${entry.fileName ?? entry.folderName}`);
    }
    seen.set(value, entry);
  }
};

const generateRegistry = () => {
  const decompMaps = collectDecompMaps();
  const runtimeMaps = collectRuntimeMaps();
  assertUnique(decompMaps, 'id', 'decomp map id');
  assertUnique(runtimeMaps, 'id', 'runtime map id');

  const runtimeById = new Map(runtimeMaps.map((entry) => [entry.id, entry]));
  const missingDecompIds = runtimeMaps
    .filter((entry) => !decompMaps.some((map) => map.id === entry.id))
    .map((entry) => entry.id);
  if (missingDecompIds.length > 0) {
    throw new Error(`Runtime maps not found in data/maps: ${missingDecompIds.join(', ')}`);
  }

  const importLines = runtimeMaps.map((entry) =>
    `import ${entry.importName} from './maps/${entry.fileName}';`
  );

  const registryEntries = decompMaps.map((map) => {
    const runtimeMap = runtimeById.get(map.id);
    const sourceValue = runtimeMap ? runtimeMap.importName : 'null';
    return [
      `  ${JSON.stringify(map.id)}: {`,
      `    id: ${JSON.stringify(map.id)},`,
      `    name: ${JSON.stringify(map.name)},`,
      `    decompFolder: ${JSON.stringify(map.folderName)},`,
      `    source: ${sourceValue}`,
      '  }'
    ].join('\n');
  });

  return `${[
    '// Generated by scripts/generate-map-registry.mjs. Do not edit by hand.',
    ...importLines,
    '',
    'export interface RegisteredMapSource {',
    '  id: string;',
    '  name: string;',
    '  decompFolder: string;',
    '  source: unknown | null;',
    '}',
    '',
    `export const DECOMP_MAP_COUNT = ${decompMaps.length};`,
    `export const EXPORTED_DECOMP_MAP_COUNT = ${runtimeMaps.length};`,
    '',
    'export const mapRegistry: Record<string, RegisteredMapSource> = {',
    registryEntries.join(',\n'),
    '};',
    '',
    'export const allDecompMapIds = Object.freeze(Object.keys(mapRegistry));',
    'export const exportedDecompMapIds = Object.freeze(',
    '  allDecompMapIds.filter((mapId) => mapRegistry[mapId].source !== null)',
    ');',
    ''
  ].join('\n')}`;
};

fs.writeFileSync(registryPath, generateRegistry());
