export function GetMapPreviewScreenIdx(mapsec: number): number {
  for (let i = 0; i < MPS_COUNT; i += 1) {
    if (sMapPreviewScreenData[i].mapsec === mapsec) {
      return i;
    }
  }
  return MPS_COUNT;
}

export function MapHasPreviewScreen(mapsec: number, type: number): boolean {
  const idx = GetMapPreviewScreenIdx(mapsec);
  if (idx !== MPS_COUNT) {
    if (type === MPS_TYPE_ANY) {
      return true;
    } else {
      return sMapPreviewScreenData[idx].type === type ? true : false;
    }
  } else {
    return false;
  }
}

export function MapHasPreviewScreen_HandleQLState2(runtime: MapPreviewRuntime, mapsec: number, type: number): boolean {
  if (runtime.gQuestLogState === QL_STATE_PLAYBACK) {
    return false;
  } else {
    return MapHasPreviewScreen(mapsec, type);
  }
}

export function MapPreview_InitBgs(runtime: MapPreviewRuntime): void {
  runtime.log.push(`InitBgsFromTemplates:0:${sMapPreviewBgTemplate.length}`);
  runtime.log.push('ShowBg:0');
}

export function MapPreview_LoadGfx(runtime: MapPreviewRuntime, mapsec: number): void {
  const idx = GetMapPreviewScreenIdx(mapsec);
  if (idx !== MPS_COUNT) {
    const info = sMapPreviewScreenData[idx];
    runtime.log.push('ResetTempTileDataBuffers');
    runtime.log.push(`LoadPalette:${info.palptr}:${BG_PLTT_ID(13)}:${3 * PLTT_SIZE_4BPP}`);
    runtime.log.push(`DecompressAndCopyTileDataToVram:0:${info.tilesptr}:0:0:0`);
    if (runtime.bgTilemapBuffers[0] === null) {
      runtime.bgTilemapBuffers[0] = { size: BG_SCREEN_SIZE };
      runtime.sAllocedBg0TilemapBuffer = true;
    } else {
      runtime.sAllocedBg0TilemapBuffer = false;
    }
    runtime.log.push(`CopyToBgTilemapBuffer:0:${info.tilemapptr}:0:0`);
    runtime.log.push('CopyBgTilemapBufferToVram:0');
  }
}

export function MapPreview_Unload(runtime: MapPreviewRuntime, windowId: number): void {
  runtime.windows.delete(windowId);
  runtime.log.push(`RemoveWindow:${windowId}`);
  if (runtime.sAllocedBg0TilemapBuffer) {
    runtime.log.push('Free:bg0TilemapBuffer');
    runtime.bgTilemapBuffers[0] = null;
  }
}

export function MapPreview_IsGfxLoadFinished(runtime: MapPreviewRuntime): boolean {
  return runtime.tempTileDataBusy;
}
const FreeTempTileDataBuffersIfPossible = MapPreview_IsGfxLoadFinished;
const IsDma3ManagerBusyWithBgCopy = (runtime: MapPreviewRuntime): boolean => runtime.dma3BusyWithBgCopy;
const IsWeatherNotFadingIn = (runtime: MapPreviewRuntime): boolean => runtime.weatherNotFadingIn;

const CreateTask = (runtime: MapPreviewRuntime, priority: number): number => {
  const id = runtime.tasks.length;
  runtime.tasks.push({ func: 'Task_RunMapPreviewScreenForest', priority, data: Array(16).fill(0), destroyed: false });
  runtime.log.push(`CreateTask:Task_RunMapPreviewScreenForest:${priority}`);
  return id;
};

export const SetGpuReg = (runtime: MapPreviewRuntime, reg: number, value: number): void => {
  runtime.gpuRegs.set(reg, value & 0xffff);
  runtime.log.push(`SetGpuReg:${reg}:${value & 0xffff}`);
};
export const GetGpuReg = (runtime: MapPreviewRuntime, reg: number): number => runtime.gpuRegs.get(reg) ?? 0;
export const SetGpuRegBits = (runtime: MapPreviewRuntime, reg: number, bits: number): void => SetGpuReg(runtime, reg, GetGpuReg(runtime, reg) | bits);
export const GetBgAttribute = (runtime: MapPreviewRuntime, bg: number, attr: number): number => runtime.bgAttributes.get(`${bg}:${attr}`) ?? 0;
export const SetBgAttribute = (runtime: MapPreviewRuntime, bg: number, attr: number, value: number): void => {
  runtime.bgAttributes.set(`${bg}:${attr}`, value);
  runtime.log.push(`SetBgAttribute:${bg}:${attr}:${value}`);
};
export const LockPlayerFieldControls = (runtime: MapPreviewRuntime): void => {
  runtime.playerFieldControlsLocked = true;
};

export function MapPreview_StartForestTransition(runtime: MapPreviewRuntime, mapsec: number): void {
  const taskId = CreateTask(runtime, 0);
  const data = runtime.tasks[taskId].data;
  data[2] = GetBgAttribute(runtime, 0, BG_ATTR_PRIORITY);
  data[4] = GetGpuReg(runtime, REG_OFFSET_BLDCNT);
  data[5] = GetGpuReg(runtime, REG_OFFSET_BLDALPHA);
  data[3] = GetGpuReg(runtime, REG_OFFSET_DISPCNT);
  data[6] = GetGpuReg(runtime, REG_OFFSET_WININ);
  data[7] = GetGpuReg(runtime, REG_OFFSET_WINOUT);
  data[10] = MapPreview_GetDuration(runtime, mapsec);
  data[8] = 16;
  data[9] = 0;
  SetBgAttribute(runtime, 0, BG_ATTR_PRIORITY, 0);
  SetGpuReg(runtime, REG_OFFSET_BLDCNT, BLDCNT_TGT1_BG0 | BLDCNT_EFFECT_BLEND | BLDCNT_TGT2_BG1 | BLDCNT_TGT2_BG2 | BLDCNT_TGT2_BG3 | BLDCNT_TGT2_OBJ | BLDCNT_TGT2_BD);
  SetGpuReg(runtime, REG_OFFSET_BLDALPHA, BLDALPHA_BLEND(16, 0));
  SetGpuRegBits(runtime, REG_OFFSET_WININ, WININ_WIN0_CLR | WININ_WIN1_CLR);
  SetGpuRegBits(runtime, REG_OFFSET_WINOUT, WINOUT_WIN01_CLR);
  data[11] = MapPreview_CreateMapNameWindow(runtime, mapsec);
  LockPlayerFieldControls(runtime);
}

export const AddWindow = (runtime: MapPreviewRuntime): number => {
  const id = runtime.nextWindowId++;
  runtime.windows.add(id);
  runtime.log.push(`AddWindow:${JSON.stringify(sMapNameWindow)}`);
  return id;
};
export const GetMapName = (runtime: MapPreviewRuntime, mapsec: number): string => runtime.mapNames.get(mapsec) ?? `MAPSEC_${mapsec}`;
export const GetStringWidth = (runtime: MapPreviewRuntime, text: string): number => runtime.stringWidths.get(text) ?? text.length * 8;

export function MapPreview_CreateMapNameWindow(runtime: MapPreviewRuntime, mapsec: number): number {
  const windowId = AddWindow(runtime);
  runtime.log.push(`FillWindowPixelBuffer:${windowId}:${PIXEL_FILL(1)}`);
  runtime.log.push(`PutWindowTilemap:${windowId}`);
  const color = [TEXT_COLOR_WHITE, TEXT_COLOR_RED, TEXT_COLOR_LIGHT_GRAY];
  const name = GetMapName(runtime, mapsec);
  const xctr = 104 - GetStringWidth(runtime, name);
  runtime.log.push(`AddTextPrinterParameterized4:${windowId}:${FONT_NORMAL}:${Math.trunc(xctr / 2)}:2:0:0:${color.join(',')}:-1:${name}`);
  return windowId;
}

export const FuncIsActiveTask = (runtime: MapPreviewRuntime): boolean => runtime.tasks.some((task) => task.func === 'Task_RunMapPreviewScreenForest' && !task.destroyed);
export function ForestMapPreviewScreenIsRunning(runtime: MapPreviewRuntime): boolean {
  if (FuncIsActiveTask(runtime) === true) {
    return false;
  } else {
    return true;
  }
}

export function Task_RunMapPreviewScreenForest(runtime: MapPreviewRuntime, taskId: number): void {
  const task = runtime.tasks[taskId];
  const data = task.data;
  switch (data[0]) {
    case 0:
      if (!FreeTempTileDataBuffersIfPossible(runtime) && !IsDma3ManagerBusyWithBgCopy(runtime)) {
        runtime.log.push(`CopyWindowToVram:${data[11]}:${COPYWIN_FULL}`);
        data[0] += 1;
      }
      break;
    case 1:
      if (!IsDma3ManagerBusyWithBgCopy(runtime)) {
        runtime.log.push('FadeInFromBlack');
        data[0] += 1;
      }
      break;
    case 2:
      if (IsWeatherNotFadingIn(runtime)) {
        runtime.log.push('Overworld_PlaySpecialMapMusic');
        data[0] += 1;
      }
      break;
    case 3:
      data[1] += 1;
      if (data[1] > data[10]) {
        data[1] = 0;
        data[0] += 1;
      }
      break;
    case 4:
      switch (data[1]) {
        case 0:
          data[9] += 1;
          if (data[9] > 16) data[9] = 16;
          break;
        case 1:
          data[8] -= 1;
          if (data[8] < 0) data[8] = 0;
          break;
      }
      data[1] = (data[1] + 1) % 3;
      SetGpuReg(runtime, REG_OFFSET_BLDALPHA, BLDALPHA_BLEND(data[8], data[9]));
      if (data[8] === 0 && data[9] === 16) {
        runtime.log.push('FillBgTilemapBufferRect_Palette0:0:0:0:0:32:32');
        runtime.log.push('CopyBgTilemapBufferToVram:0');
        data[0] += 1;
      }
      break;
    case 5:
      if (!IsDma3ManagerBusyWithBgCopy(runtime)) {
        MapPreview_Unload(runtime, data[11]);
        SetBgAttribute(runtime, 0, BG_ATTR_PRIORITY, data[2]);
        SetGpuReg(runtime, REG_OFFSET_DISPCNT, data[3]);
        SetGpuReg(runtime, REG_OFFSET_BLDCNT, data[4]);
        SetGpuReg(runtime, REG_OFFSET_BLDALPHA, data[5]);
        SetGpuReg(runtime, REG_OFFSET_WININ, data[6]);
        SetGpuReg(runtime, REG_OFFSET_WINOUT, data[7]);
        task.destroyed = true;
        runtime.log.push(`DestroyTask:${taskId}`);
      }
      break;
  }
}

export function GetDungeonMapPreviewScreenInfo(mapsec: number): MapPreviewScreen | null {
  const idx = GetMapPreviewScreenIdx(mapsec);
  if (idx === MPS_COUNT) return null;
  return sMapPreviewScreenData[idx];
}

export const FlagGet = (runtime: MapPreviewRuntime, flagId: number): boolean => runtime.flags.has(flagId);
export const FlagSet = (runtime: MapPreviewRuntime, flagId: number): void => {
  runtime.flags.add(flagId);
};

export function MapPreview_GetDuration(runtime: MapPreviewRuntime, mapsec: number): number {
  const idx = GetMapPreviewScreenIdx(mapsec);
  if (idx === MPS_COUNT) return 0;
  const flagId = sMapPreviewScreenData[idx].flagId;
  if (sMapPreviewScreenData[idx].type === MPS_TYPE_CAVE) {
    if (!FlagGet(runtime, flagId)) return 120;
    return 40;
  } else {
    if (runtime.sHasVisitedMapBefore) return 120;
    return 40;
  }
}

export function MapPreview_SetFlag(runtime: MapPreviewRuntime, flagId: number): void {
  if (!FlagGet(runtime, flagId)) {
    runtime.sHasVisitedMapBefore = true;
  } else {
    runtime.sHasVisitedMapBefore = false;
  }
  FlagSet(runtime, flagId);
}
