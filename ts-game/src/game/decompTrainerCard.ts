export const Task_TrainerCard = (
  data: TrainerCardData,
  taskId: number,
  inputs: TrainerCardTaskInputs = {}
): void => {
  switch (data.mainState) {
    case 0:
      if (!inputs.isDma3ManagerBusyWithBgCopy) {
        recordRuntimeCall(data, 'FillWindowPixelBuffer', 1, 'PIXEL_FILL(0)');
        data.mainState++;
      }
      break;
    case 1:
      if (PrintAllOnCardFront(data))
        data.mainState++;
      break;
    case 2:
      DrawTrainerCardWindow(data, 1);
      data.mainState++;
      break;
    case 3:
      recordRuntimeCall(data, 'FillWindowPixelBuffer', 2, 'PIXEL_FILL(0)');
      CreateTrainerCardTrainerPic(data);
      DrawTrainerCardWindow(data, 2);
      data.mainState++;
      break;
    case 4:
      DrawCardScreenBackground(data, data.bgTilemap);
      data.mainState++;
      break;
    case 5:
      DrawCardFrontOrBack(data, data.frontTilemap);
      data.mainState++;
      break;
    case 6:
      DrawStarsAndBadgesOnCard(data);
      data.mainState++;
      break;
    case 7:
      if (inputs.wirelessCommType === 1 && inputs.receivedRemoteLinkPlayers === true) {
        recordRuntimeCall(data, 'LoadWirelessStatusIndicatorSpriteGfx');
        recordRuntimeCall(data, 'CreateWirelessStatusIndicatorSprite', 230, 150);
      }
      recordRuntimeCall(data, 'BeginNormalPaletteFade', 'PALETTES_ALL', 0, 16, 0, 'RGB_BLACK');
      data.callbacks.vblank = 'VBlankCB_TrainerCard';
      data.mainState++;
      break;
    case 8:
      if (!inputs.updatePaletteFade && !inputs.isDma3ManagerBusyWithBgCopy) {
        recordRuntimeCall(data, 'PlaySE', 'SE_CARD_OPEN');
        data.mainState = STATE_HANDLE_INPUT_FRONT;
      }
      break;
    case 9:
      if (!inputs.isSEPlaying)
        data.mainState++;
      break;
    case STATE_HANDLE_INPUT_FRONT:
      if (!inputs.receivedRemoteLinkPlayers && data.timeColonNeedDraw) {
        PrintTimeOnCard(data);
        DrawTrainerCardWindow(data, 1);
        data.timeColonNeedDraw = false;
      }

      if (inputs.joyNewA) {
        data.helpContext = HELPCONTEXT_TRAINER_CARD_BACK;
        FlipTrainerCard(data);
        recordRuntimeCall(data, 'PlaySE', 'SE_CARD_FLIP');
        data.mainState = STATE_WAIT_FLIP_TO_BACK;
      } else if (inputs.joyNewB) {
        if (inputs.receivedRemoteLinkPlayers && data.isLink && inputs.inUnionRoom === true) {
          data.mainState = STATE_WAIT_LINK_PARTNER;
        } else {
          recordRuntimeCall(data, 'BeginNormalPaletteFade', 'PALETTES_ALL', 0, 0, 16, 'RGB_BLACK');
          data.mainState = STATE_CLOSE_CARD;
        }
      }
      break;
    case STATE_WAIT_FLIP_TO_BACK:
      if (IsCardFlipTaskActive(data) && inputs.overworldLinkRecvQueueLengthMoreThan2 !== true) {
        recordRuntimeCall(data, 'PlaySE', 'SE_CARD_OPEN');
        data.mainState = STATE_HANDLE_INPUT_BACK;
      }
      break;
    case STATE_HANDLE_INPUT_BACK:
      if (inputs.joyNewB) {
        if (inputs.receivedRemoteLinkPlayers && data.isLink && inputs.inUnionRoom === true) {
          data.mainState = STATE_WAIT_LINK_PARTNER;
        } else if (inputs.receivedRemoteLinkPlayers) {
          recordRuntimeCall(data, 'BeginNormalPaletteFade', 'PALETTES_ALL', 0, 0, 16, 'RGB_BLACK');
          data.mainState = STATE_CLOSE_CARD;
        } else {
          data.helpContext = HELPCONTEXT_TRAINER_CARD_FRONT;
          FlipTrainerCard(data);
          data.mainState = STATE_WAIT_FLIP_TO_FRONT;
          recordRuntimeCall(data, 'PlaySE', 'SE_CARD_FLIP');
        }
      } else if (inputs.joyNewA) {
        if (inputs.receivedRemoteLinkPlayers && data.isLink && inputs.inUnionRoom === true) {
          data.mainState = STATE_WAIT_LINK_PARTNER;
        } else {
          recordRuntimeCall(data, 'BeginNormalPaletteFade', 'PALETTES_ALL', 0, 0, 16, 'RGB_BLACK');
          data.mainState = STATE_CLOSE_CARD;
        }
      }
      break;
    case STATE_WAIT_LINK_PARTNER:
      recordRuntimeCall(data, 'SetCloseLinkCallback');
      recordRuntimeCall(data, 'DrawDialogueFrame', 0, 1);
      recordRuntimeCall(data, 'AddTextPrinterParameterized', 0, FONT_NORMAL, 'gText_WaitingTrainerFinishReading', 0, 1, 'TEXT_SKIP_DRAW', 0);
      recordRuntimeCall(data, 'CopyWindowToVram', 0, 'COPYWIN_FULL');
      data.mainState = STATE_CLOSE_CARD_LINK;
      break;
    case STATE_CLOSE_CARD_LINK:
      if (!inputs.receivedRemoteLinkPlayers) {
        recordRuntimeCall(data, 'BeginNormalPaletteFade', 'PALETTES_ALL', 0, 0, 16, 'RGB_BLACK');
        data.mainState = STATE_CLOSE_CARD;
      }
      break;
    case STATE_CLOSE_CARD:
      if (!inputs.updatePaletteFade)
        CloseTrainerCard(data, taskId);
      break;
    case STATE_WAIT_FLIP_TO_FRONT:
      if (IsCardFlipTaskActive(data) && inputs.overworldLinkRecvQueueLengthMoreThan2 !== true) {
        data.mainState = STATE_HANDLE_INPUT_FRONT;
        recordRuntimeCall(data, 'PlaySE', 'SE_CARD_OPEN');
      }
      break;
  }
};

