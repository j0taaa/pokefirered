const toU16 = (value: number): number => value & 0xffff;
const toU32 = (value: number): number => value >>> 0;
const u32Div = (numerator: number, denominator: number): number => Math.trunc(toU32(numerator) / toU32(denominator)) >>> 0;
const toU8 = (value: number): number => value & 0xff;

export const WIN_RANGE = (a: number, b: number): number => ((a & 0xff) << 8) | (b & 0xff);

export const formatDecimalRightAlign = (value: number, width: number): string => Math.trunc(value).toString().padStart(width, ' ');
export const formatDecimalLeadingZeros = (value: number, width: number): string => Math.trunc(value).toString().padStart(width, '0');
export const formatDecimalLeftAlign = (value: number, _width: number): string => Math.trunc(value).toString();

export const GetCappedGameStat = (
  statId: number,
  maxValue: number,
  getGameStat: (statId: number) => number
): number => Math.min(maxValue, getGameStat(statId));

export const GetTrainerStarCount = (trainerCard: TrainerCard): number => {
  let stars = 0;

  if (
    trainerCard.rse.hofDebutHours !== 0 ||
    trainerCard.rse.hofDebutMinutes !== 0 ||
    trainerCard.rse.hofDebutSeconds !== 0
  )
    stars++;

  if (trainerCard.rse.caughtAllHoenn)
    stars++;

  if (trainerCard.rse.battleTowerStraightWins > 49)
    stars++;

  if (trainerCard.rse.hasAllPaintings)
    stars++;

  return stars;
};

export const SetPlayerCardData = (
  trainerCard: TrainerCard,
  cardType: number,
  sources: TrainerCardPlayerDataSources
): void => {
  let playTime: number;
  let i: number;

  trainerCard.rse.gender = sources.saveBlock2.playerGender;
  trainerCard.rse.playTimeHours = sources.saveBlock2.playTimeHours;
  trainerCard.rse.playTimeMinutes = sources.saveBlock2.playTimeMinutes;

  playTime = sources.getGameStat(GAME_STAT_FIRST_HOF_PLAY_TIME);
  if (!sources.getGameStat(GAME_STAT_ENTERED_HOF))
    playTime = 0;

  trainerCard.rse.hofDebutHours = playTime >> 16;
  trainerCard.rse.hofDebutMinutes = (playTime >> 8) & 0xff;
  trainerCard.rse.hofDebutSeconds = playTime & 0xff;
  if ((playTime >> 16) > 999) {
    trainerCard.rse.hofDebutHours = 999;
    trainerCard.rse.hofDebutMinutes = 59;
    trainerCard.rse.hofDebutSeconds = 59;
  }

  trainerCard.rse.hasPokedex = sources.flagGet(FLAG_SYS_POKEDEX_GET);
  trainerCard.rse.caughtAllHoenn = sources.hasAllHoennMons();
  trainerCard.rse.caughtMonsCount = sources.getCaughtMonsCount();

  trainerCard.rse.trainerId = ((sources.saveBlock2.playerTrainerId[1] ?? 0) << 8) | (sources.saveBlock2.playerTrainerId[0] ?? 0);

  trainerCard.rse.linkBattleWins = GetCappedGameStat(GAME_STAT_LINK_BATTLE_WINS, 9999, sources.getGameStat);
  trainerCard.rse.linkBattleLosses = GetCappedGameStat(GAME_STAT_LINK_BATTLE_LOSSES, 9999, sources.getGameStat);
  trainerCard.rse.pokemonTrades = GetCappedGameStat(GAME_STAT_POKEMON_TRADES, 0xffff, sources.getGameStat);

  trainerCard.rse.battleTowerWins = 0;
  trainerCard.rse.battleTowerStraightWins = 0;
  trainerCard.rse.contestsWithFriends = 0;
  trainerCard.rse.pokeblocksWithFriends = 0;

  trainerCard.rse.hasAllPaintings = false;

  trainerCard.rse.money = sources.getMoney(sources.saveBlock1.money);

  for (i = 0; i < TRAINER_CARD_PROFILE_LENGTH; i += 1)
    trainerCard.rse.easyChatProfile[i] = sources.saveBlock1.easyChatProfile[i] ?? 0;

  trainerCard.rse.playerName = sources.saveBlock2.playerName;

  if (cardType === CARD_TYPE_FRLG) {
    trainerCard.rse.stars = GetTrainerStarCount(trainerCard);
  } else if (cardType === CARD_TYPE_RSE) {
    trainerCard.rse.stars = 0;
    if (
      trainerCard.rse.hofDebutHours !== 0 ||
      (trainerCard.rse.hofDebutMinutes !== 0 || trainerCard.rse.hofDebutSeconds !== 0)
    )
      trainerCard.rse.stars = cardType;

    if (sources.hasAllKantoMons())
      trainerCard.rse.stars++;

    if (sources.hasAllMons())
      trainerCard.rse.stars++;
  }
};

