export const LoadCardGfx = (data: TrainerCardData): boolean => {
  switch (data.gfxLoadState) {
    case 0:
      lz77UnCompWram(data, data.cardType === CARD_TYPE_RSE ? 'sHoennTrainerCardBg_Tilemap' : 'sKantoTrainerCardBg_Tilemap', 'bgTilemap');
      break;
    case 1:
      lz77UnCompWram(data, data.cardType === CARD_TYPE_RSE ? 'sHoennTrainerCardBack_Tilemap' : 'sKantoTrainerCardBack_Tilemap', 'backTilemap');
      break;
    case 2:
      if (!data.isLink)
        lz77UnCompWram(data, data.cardType === CARD_TYPE_RSE ? 'sHoennTrainerCardFront_Tilemap' : 'sKantoTrainerCardFront_Tilemap', 'frontTilemap');
      else
        lz77UnCompWram(data, data.cardType === CARD_TYPE_RSE ? 'sHoennTrainerCardFrontLink_Tilemap' : 'sKantoTrainerCardFrontLink_Tilemap', 'frontTilemap');
      break;
    case 3:
      lz77UnCompWram(data, 'sKantoTrainerCardBadges_Gfx', 'badgeTiles');
      break;
    case 4:
      lz77UnCompWram(data, data.cardType === CARD_TYPE_RSE ? 'gHoennTrainerCard_Gfx' : 'gKantoTrainerCard_Gfx', 'cardTiles');
      break;
    case 5:
      if (data.cardType === CARD_TYPE_FRLG)
        lz77UnCompWram(data, 'sTrainerCardStickers_Gfx', 'stickerTiles');
      break;
    default:
      data.gfxLoadState = 0;
      return true;
  }
  data.gfxLoadState += 1;
  return false;
};

export const SetTrainerCardBgsAndPals = (data: TrainerCardData): boolean => {
  switch (data.bgPalLoadState) {
    case 0:
      loadBgTiles(data, 3, 'badgeTiles', 0x80 * NUM_BADGES, 0);
      break;
    case 1:
      loadBgTiles(data, 0, 'cardTiles', 0x1800, 0);
      break;
    case 2:
      loadPalette(
        data,
        data.cardType === CARD_TYPE_RSE
          ? `sHoennTrainerCardPals[${data.trainerCard.rse.stars}]`
          : `sKantoTrainerCardPals[${data.trainerCard.rse.stars}]`,
        bgPlttId(0),
        3 * PLTT_SIZE_4BPP
      );
      break;
    case 3:
      loadPalette(
        data,
        data.cardType === CARD_TYPE_RSE ? 'sHoennTrainerCardBadges_Pal' : 'sKantoTrainerCardBadges_Pal',
        bgPlttId(3),
        PLTT_SIZE_4BPP
      );
      break;
    case 4:
      if (data.cardType === CARD_TYPE_RSE && data.trainerCard.rse.gender !== MALE)
        loadPalette(data, 'sHoennTrainerCardFemaleBg_Pal', bgPlttId(1), PLTT_SIZE_4BPP);
      else if (data.trainerCard.rse.gender !== MALE)
        loadPalette(data, 'sKantoTrainerCardFemaleBg_Pal', bgPlttId(1), PLTT_SIZE_4BPP);
      break;
    case 5:
      loadPalette(data, 'sTrainerCardStar_Pal', bgPlttId(4), PLTT_SIZE_4BPP);
      break;
    case 6:
      setBgTilemapBuffer(data, 0, 'cardTilemapBuffer');
      setBgTilemapBuffer(data, 2, 'bgTilemapBuffer');
      break;
    default:
      fillBgTilemapBufferRectPalette0(data, 0, 0, 0, 0, 32, 32);
      fillBgTilemapBufferRectPalette0(data, 2, 0, 0, 0, 32, 32);
      fillBgTilemapBufferRectPalette0(data, 3, 0, 0, 0, 32, 32);
      return true;
  }

  data.bgPalLoadState += 1;
  return false;
};

export const DrawStarsAndBadgesOnCard = (data: TrainerCardData): void => {
  let tileNum = 192;
  const palNum = 3;

  fillBgTilemapBufferRect(data, 3, 143, 15, sStarYOffsets[data.cardType], data.trainerCard.rse.stars, 1, 4);
  if (!data.isLink) {
    let x = 4;
    for (let i = 0; i < NUM_BADGES; i++, tileNum += 2, x += 3) {
      if (data.hasBadge[i]) {
        fillBgTilemapBufferRect(data, 3, tileNum, x, 16, 1, 1, palNum);
        fillBgTilemapBufferRect(data, 3, tileNum + 1, x + 1, 16, 1, 1, palNum);
        fillBgTilemapBufferRect(data, 3, tileNum + 16, x, 17, 1, 1, palNum);
        fillBgTilemapBufferRect(data, 3, tileNum + 17, x + 1, 17, 1, 1, palNum);
      }
    }
  }
  recordRuntimeCall(data, 'CopyBgTilemapBufferToVram', 3);
};

