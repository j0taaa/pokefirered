const drawCardTilemap = (dest: number[], source: readonly number[]): void => {
  for (let i = 0; i < 20; i++) {
    for (let j = 0; j < 32; j++) {
      if (j < 30)
        dest[32 * i + j] = source[30 * i + j] ?? 0;
      else
        dest[32 * i + j] = source[0] ?? 0;
    }
  }
};

